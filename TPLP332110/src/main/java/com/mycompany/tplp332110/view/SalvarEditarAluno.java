    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(fmtTelefone.getText().length() == 15 &&
                cbTipo.getSelectedIndex() != 0)
            if(cbTipo.getSelectedIndex() == 5 && txtOutros.getText().length() < 3) {
                JOptionPane.showMessageDialog(pnCampos, "O campo Outros deve ser preenchido");
            }
            else {
                if(foneEditIndex != -1) {
                    if(cbTipo.getSelectedIndex() == 5)
                        modelo.setElementAt(fmtTelefone.getText() + "     " + txtOutros.getText(), foneEditIndex);
                    else
                        modelo.setElementAt(fmtTelefone.getText() + "     " + cbTipo.getSelectedItem().toString(), foneEditIndex);
                    foneEditIndex = -1;
                }                
                else if(cbTipo.getSelectedIndex() == 5 && txtOutros.getText().length() >= 3) {
                    modelo.addElement(fmtTelefone.getText() + "     " + txtOutros.getText());
                }
                else {
                    modelo.addElement(fmtTelefone.getText() + "     " + cbTipo.getSelectedItem().toString());
                }
                
                cbTipo.setSelectedIndex(0);
                txtOutros.setText("");
                lbOutros.setVisible(false);
                txtOutros.setVisible(false);
                btnAdd.setEnabled(false);
                fmtTelefone.setValue(null);
                fmtTelefone.requestFocusInWindow();
                fmtTelefone.setCaretPosition(1);
                btnExcluir.setEnabled(false);
            }
    }//GEN-LAST:event_btnAddActionPerformed

