    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        
        String options[] = {"Continuar", "Cancelar"};
        int val = JOptionPane.showOptionDialog(pnBotoes, 
                        "A exclusão será definitiva.\nConfirme a exclusão do Telefone:\n" 
                        + modelo.getElementAt(foneEditIndex),
                        "Antenção!",
                        JOptionPane.DEFAULT_OPTION,
                        JOptionPane.WARNING_MESSAGE, 
                        null,
                        options,
                        options[0]);
        if(val == 0) {
            modelo.remove(foneEditIndex);
            foneEditIndex = -1;
            fmtTelefone.setText("");
            cbTipo.setSelectedIndex(0);
            txtOutros.setText("");
            lbOutros.setVisible(false);
            txtOutros.setVisible(false);
            btnAdd.setEnabled(false);
            fmtTelefone.requestFocusInWindow();
            fmtTelefone.setCaretPosition(1);
            btnExcluir.setEnabled(false);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void listTelefonesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listTelefonesMouseClicked
        if(evt.getClickCount() == 2) {
            foneEditIndex = listTelefones.getSelectedIndex();
            String campos[] = listTelefones.getSelectedValue().split("     ");
            fmtTelefone.setText(campos[0]);
            int idTipo = 5;
            for(int i = 1; i < cbTipo.getItemCount()-1; i++) {
                if(cbTipo.getItemAt(i).equalsIgnoreCase(campos[1])) {
                    idTipo = i;
                    break;
                }
            }
            
            cbTipo.setSelectedIndex(idTipo);
            if(idTipo == 5) {
                lbOutros.setVisible(true);
                txtOutros.setVisible(true);
                txtOutros.setText(campos[1]);
            }
            btnExcluir.setEnabled(true);
        }
    }//GEN-LAST:event_listTelefonesMouseClicked

