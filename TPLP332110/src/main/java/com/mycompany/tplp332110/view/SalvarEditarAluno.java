    private void fmtTelefoneKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fmtTelefoneKeyTyped
        if (fmtTelefone.getText().length() == 15
                && cbTipo.getSelectedIndex() != 0) { // 15 caracteres ()-9 números e um espaço
            btnAdd.setEnabled(true);
            if (cbTipo.getSelectedIndex() == 5) {// 5 id de outros
                lbOutros.setVisible(true);
                txtOutros.setVisible(true);
            } else {
                lbOutros.setVisible(false);
                txtOutros.setVisible(false);
            }
        } else {
            btnAdd.setEnabled(false);
            if (cbTipo.getSelectedIndex() == 5) {// 5 índice do tipo: outros no JList
                lbOutros.setVisible(true);
                txtOutros.setVisible(true);
            } else {
                lbOutros.setVisible(false);
                txtOutros.setVisible(false);
            }
        }
    }//GEN-LAST:event_fmtTelefoneKeyTyped

